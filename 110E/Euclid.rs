fn gcd( _m: i32, _n: i32 ) -> i32 {
    let mut m: i32 = _m;
    let mut n: i32 = _n;
    let mut r: i32; 
    while n > 0 {
        r = m % n;
        m = n;
        n = r; 
    }
    m
}
