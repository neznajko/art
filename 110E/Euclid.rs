use std::io::{ self, Write };
////////////////////////////////////////////////////////////////
fn getint() -> i32 {
    loop {
        print!( "Enter some positive integer: " );
        io::stdout().flush().unwrap();
        let mut input_line = String::new();
        io::stdin()
            .read_line( &mut input_line )
            .expect( "2:) boom" );
        let n: i32 = input_line
                     .trim()
                     .parse()
                     .expect( "@;' check thus" );
        if n > 0 {
            return n;
        }
    }
}
