const key = [ 0, 8, 7, 6, 5, 4, 3, 2, 1 ];
////////////////////////////////////////////////////////////////
const n = key.length - 1;
////////////////////////////////////////////////////////////////
let count = Array( n + 1 ).fill( 0 );
////////////////////////////////////////////////////////////////
let i = n;
////////////////////////////////////////////////////////////////
while( i > 1 ){
    let j = i - 1;
    while( j > 0 ){
        if( key[ i ] < key[ j ]){
            count[ j ]++;
        } else {
            count[ i ]++;
        }
        j--;
    }
    i--;
}
