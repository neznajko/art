main:   clear /* ================================== your mind */
	lea X(rip),r15 /* --------------------------- r15 = X */
	mov $0,ebx /* ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ prev <- 0 */
        mov $N,ecx /* _____________________________ curr <- N */
1:	cmp $0,ecx /* ///////////////////////////// curr = 0? */
	jz 4f /* ,,,,,,,,,,,,,,, done, check thus: x/8dw $r15 */
	cmpl $0,(r15,rcx,4) /* ;;;;;;;;;;;;;;;;; X[curr] < 0? */
	jge 2f /* ``````````````````````````````````````` nop */
	negl (r15,rcx,4) /* +-+-+-+-+-+-+ X[curr] <- -X[curr] */
	dec ecx /* ,,,,;;,;;;,,,;;,,,,,;;;;;,,,,,;;;;, --curr */
	jmp 1b /* --_--_--_--_--_--_--_--_--_-- let's go back */
2:	mov (r15,rcx,4),edx /* \\\\\\\\\\\\\\ next <- X[curr] */
	neg ebx /* @@@@@@@@@@@@@@@@@@@@@@@@@@@@@@ ebx <- -ebx */
	mov ebx,(r15,rcx,4) /* _____... .  . X[curr] <- -prev */
	neg ebx /* ***************************** re-establish */
	cmp $0,edx /* x x x x x x x x x x x x x x x next = 0? */
	jnz 3f /* ....ooo o o O  O   O   O  O O o ooo.... nop */
	mov $0,ebx /* vxvxvxvxvxvxvxvxvxvxvxvxvxvxv prev <- 0 */
	jmp 1b /* .... . . . . .  .  . . . . . ... next cycle */
3:	mov ecx,ebx /* ^^^^^^^^^^^^^^^^^^^^^^^^^ prev <- curr */
	mov edx,ecx /* vvv v v v V V V V v v vvv curr <- next */
	jmp 2b /* ^V^V^V^V^V^V^V^V^V^V^V^V^V^V check this out */
4:	ret /* ,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,, g{zurückgeben} */
