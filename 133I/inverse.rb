def inverse_in_place( x )                     # #######  =======
  prev = 0                                    #                 
  curr = x.length - 1                         # #######  =======
  while curr > 0                              # #              =
    if x[ curr ] < 0                          # /////      88888
      x[ curr ] = -x[ curr ]                  # ////  /  8  8888
      curr -= 1                               # //  ///  888  88
    else                                      #   /////  88888  
      while true                              # кінець          末
        _next = x[ curr ]                     # Ende            
        x[ curr ] = -prev                     # fin             
        if _next == 0                         # fin 
          prev = 0                            # fine            す
          break                               # end             え
        end                                   ##################
        prev = curr                           ##################
        curr = _next                          ##################
      end                                     ################## 
    end                                       ##################
  end                                         ################## 
end                                           ##################
